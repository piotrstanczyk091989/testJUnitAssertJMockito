package pl.javaps.p2;

public class Calculator {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int subtract(int a, int b) {
        return a - b;
    }

    public static int multiply(int a, int b) {
        return a * b;
    }

    public static float divide(float a, float b) {
        return a / b;
    }
}
